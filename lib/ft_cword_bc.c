#include "libft.h"

int		ft_cword_bc(char *str, char c)
{
	int		i;
	int		co;

	i = 0;
	co = 0;
	if (str)
	{
		while (str[i] != '\0')
		{
			if (str[i] != c)
			{
				while (str[i] != '\0' && str[i] != c)
					i++;
				co++;
			}
			else
				i++;
		}
	}
	return (co);
}
